/* Описание: Вывод на экран ctg в промежутке [–1, 1], с шагом 0.001 */
/**********************************************************/
var i = -1;
while (i < 1) {
    i = i + 0.001;
    result  = Math.cos(i) / Math.sin(i);
    WScript.Echo( "ctg(" + i + ") = " + result );
}